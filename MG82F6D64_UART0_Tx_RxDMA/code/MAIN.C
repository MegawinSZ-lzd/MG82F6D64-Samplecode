/***********************************************************************************
函数名称:   void InitSystem(void)
功能描述:   系统初始化
输入参数:   
返回参数:     
*************************************************************************************/
void InitSystem(void)
{
	InitPort();
	InitClock();
	InitDMA();
	InitUart0_S0BRG();
	InitInterrupt();	
	
	INT_EnAll();	// 使能全局中断
}

void main()
{
	u8 i;
	InitSystem();
	SendStr("Start!\n");
	LED_G_1=0;LED_R=0;
	DelayXms(1000);
	LED_G_1=1;LED_R=1;	

	DMA_Start();	//启动DMA
	RI0=0;	// 清串口接收标志

	while(1)
	{
		DelayXms(100);
		LED_G_0 = !LED_G_0;
		if(bDMAFlag)
		{// 收到一组数据
			bDMAFlag=0;
			SendStr("\nRx:");
			for(i=0;i<(UART0_RX_BUFF_SIZE);i++)
			{
				SendHex(RcvBuf[i]);
				SendByte(' ');
			}
			DMA_Start();	//启动DMA
			RI0=0;	// 清串口接收标志
		}

	}

}


